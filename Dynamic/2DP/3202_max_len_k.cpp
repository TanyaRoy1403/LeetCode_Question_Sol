int maxLen(vector<int>&arr,int k){
    vector<vector<int>>dp(k, vector<int>(k,0));
    int len=0;
    for(auto it:arr){
        int curr_parity=it%k;
        for(int prev=0;prev<k;prev++){
            dp[prev][curr_parity]=dp[curr_parity][prev]+1;
            len=max(len,dp[prev][curr_parity]);
        }
    }
    return len;
}
